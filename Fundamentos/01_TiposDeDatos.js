/**
 *  Tipos de datos
 */

// Numero - number
console.log(27);
console.log(27.57);

// Texto - string
console.log("Yair Osorio Meza");
console.log("Yair Osorio Meza");
console.log(`Hola soy Yair`);

// Logicos - booleans
console.log(true);
console.log(false);

// Indefinido - undefined
console.log(undefined);

// Nulo - null
console.log(null);

/**
 *  Tipos de datos Estructurales
 */

// Objeto - object
console.log({
  nombre: "Yair",
  edad: 31,
  fechadenacimiento: null,
  esAlto: false,
  ciudades: ["Medellin", "Bogota", "Barraquilla"],
});

// Tipos de datos de tipo lista - arreglos - array
console.log(["Colombia", "venezuela", "Ecuador"]);
console.log([31, true, { nombre: "Yair" }, [1, 2, 3, 4, 5]]);
